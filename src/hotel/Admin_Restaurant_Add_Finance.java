    private void confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmActionPerformed
         String building = building_field.getText();
         String eletricity = eletricity_field.getText();
         String water = water_field.getText();
         String health = health_field.getText();
         String phone = phone_field.getText();
         String state = state_field.getText();
          

        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hotel_managment","root","bajrami27");

            String query = "call restaurant_finance_procedure('" + building + "','" + eletricity + "','" + water + "','" + health + "','" + phone + "','" + state + "')";

            Statement stmt = con.createStatement();
            int x = stmt.executeUpdate(query);

            if(building.equals("") || eletricity.equals("") || water.equals("") || health.equals("") || phone.equals("") || state.equals("")){
                JOptionPane.showMessageDialog(null,"Please fill all the requirement","title",JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Finance inserted","title",JOptionPane.INFORMATION_MESSAGE);
                building_field.setText(null);
                eletricity_field.setText(null);
                water_field.setText(null);
                health_field.setText(null);
                phone_field.setText(null);
                state_field.setText(null);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Somethink went wrong with connection","title",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_confirmActionPerformed

