    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
           String name = name_edit.getText();
           String surname = surname_edit.getText();
           String email = email_edit.getText();
           String password = password_edit.getText();
           String security_question = question_edit.getSelectedItem().toString();
           String security_answer = answer_edit.getText();
           
           try{
             Connection con = java.sql.DriverManager.getConnection("jdbc:mysql://localhost:3306/hotel_managment","root","bajrami27");
             
             String query = "call update_procedure('" + name + "','" + surname + "','" + email + "','" + password + "','" + security_question + "','" + security_answer + "')";

             Statement stmt = con.createStatement();
             int x = stmt.executeUpdate(query);
             
             
             
             
             if(name.equals("") || surname.equals("") || email.equals("") || password.equals("") || security_answer.equals("")){
                 JOptionPane.showMessageDialog(null,"Please fill all the requirement","title",JOptionPane.INFORMATION_MESSAGE);
             }else{
                 JOptionPane.showMessageDialog(null, "Update went success","title",JOptionPane.INFORMATION_MESSAGE);
                 name_edit.setText(null);
                 surname_edit.setText(null);
                 email_edit.setText(null);
                 password_edit.setText(null);
                 answer_edit.setText(null);
             }             
               
           }catch(Exception e){
              JOptionPane.showMessageDialog(null,"Somethink went wrong with connection","title",JOptionPane.ERROR_MESSAGE);
           }
    }//GEN-LAST:event_updateActionPerformed

