    private void enter_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enter_buttonActionPerformed
       
          try{
              Class.forName("com.mysql.cj.jdbc.Driver");
              Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hotel_managment","root","bajrami27");
              
              String email = login_email.getText();
              String password = login_password.getText();
              
              Statement stmt = con.createStatement();
              
              String query = "select * from client where email ='" + email + "' and password = '" + password + "'";
              
              ResultSet rs = stmt.executeQuery(query);
              
              if(rs.next()){
                  User_Area user_area = new User_Area();
                  user_area.show();
                  dispose();
              }else if(email.equals("") || password.equals("")){
                  JOptionPane.showMessageDialog(null,"Please fill the requirement","title",JOptionPane.INFORMATION_MESSAGE);
              }else{
                  JOptionPane.showMessageDialog(null,"Invalid username or password","title",JOptionPane.WARNING_MESSAGE);
                  login_email.setText("");
                  login_password.setText("");
              }
          }catch(Exception e){
                  JOptionPane.showMessageDialog(null, "Somethink went wrong with connection","title",JOptionPane.ERROR_MESSAGE);
          }
    }//GEN-LAST:event_enter_buttonActionPerformed

